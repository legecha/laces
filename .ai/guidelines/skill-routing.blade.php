# Skill Routing

Load the relevant project skill before implementing work in that area.

## Project Tooling

Use the `project-tooling` skill when:

- Running project commands
- Choosing between Composer scripts
- Running validation tools
- Running formatters
- Running tests
- Working with npm scripts
- Determining the correct project workflow

## Livewire Architecture

Use the `livewire-architecture` skill when:

- Creating Livewire components
- Modifying Livewire components
- Creating pages
- Creating forms
- Implementing UI flows
- Working with FluxUI
- Working with Alpine.js inside Livewire
- Making Livewire architecture decisions
- Choosing between Livewire features

## Eloquent Models

Use the `eloquent-models` skill when:

- Creating models
- Creating migrations
- Creating relationships
- Creating enums used by models
- Creating casts
- Designing database structure
- Working with Eloquent conventions

## Actions and DTOs

Use the `actions-and-dtos` skill when:

- Creating Actions
- Creating DTOs
- Implementing business logic
- Designing workflows
- Coordinating transactions
- Dispatching events
- Designing application services

## Code Quality

Use the `code-quality` skill when:

- Writing PHP code
- Refactoring PHP code
- Reviewing code quality
- Applying project coding standards
- Applying static-analysis requirements
- Applying model conventions

## Code Structure

Use the `code-structure` skill when:

- Organising classes
- Reordering methods
- Reordering properties
- Creating new classes
- Reviewing consistency
- Applying project structure conventions

## API Design

Use the `api-design` skill when:

- Creating APIs
- Creating resource controllers
- Designing routes
- Designing resources
- Designing RESTful workflows

## Testing

Use the `testing` skill when:

- Creating tests
- Updating tests
- Creating Pest tests
- Creating smoke tests
- Creating browser tests
- Determining test coverage
- Verifying implementation completeness

When multiple skills apply, follow all relevant skills. Prefer the more specific skill when guidance overlaps.